# Operateurs logiques 

a = True
b = False

# and : Renvoie `True` si les deux expressions sont `True`.
result_and = a and b 
# or : Renvoie `True` si au moins une des expressions est `True`.
result_or = a or b
# not : Renvoie l'inverse de l'expression.
result_not_a = not a
result_not_b = not b

print(result_and)
print(result_or)
print(result_not_a)
print(result_not_b)

