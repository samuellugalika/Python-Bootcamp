# Operateurs arithmetiques
# Addition (+) : fait l'addition de deux nombres
somme = 10 + 5
# Soustraction (-) : fait la difference de deux nombres
difference = 20 - 10
# Multiplication (*): fait le produit de deux nombres
produit = 5 * 2
# Division (/) : divise deux nombres
division = 30 / 2
# Modulo (%) : donne le reste de la division 
reste = 13 % 3
# Exponentiation (**) : eleve un nombre a la puissance de l'autre
puissance = 3 ** 2
# Division_entiere (//) : donne le reste entier d'une division
division_entiere = 10 // 3

print(somme)
print(difference)
print(produit)
print(division)
print(reste)
print(puissance)
print(division_entiere)

