# Operateurs de comparaisons 
# Egal a (==) : verifie si deux valeurs sont egales
est_egal = (5 == 5)
# DiffÃ©rent de (!=) : verifie si deux valeurs sont differentes
est_different = (5 != 3)
# Plus grand que ou strictement superieur a (>) : verifie si une valeur est strictement superieure ou grand a l'autre
est_plus_grand = (10 > 7)
# Plus petit que ou strictement inferieur a (<) : verifie si une valeur est strictement inferieure ou petite a l'autre
est_plus_petit = (10 < 70)
# Plus grand ou egal a (>=) : verifie si une valeur est superieure/grand ou egale a l'autre
est_plus_grand_ou_egal = (20 >= 20) 
# Plus petit ou egal a (<=) :verifie si une valeur est inferieure/petit ou egale a l'autre
est_plus_petit_ou_egal = (19 <= 20)

print(est_egal)
print(est_different)
print(est_plus_grand)
print(est_plus_petit)
print(est_plus_grand_ou_egal)
print(est_plus_petit_ou_egal)

